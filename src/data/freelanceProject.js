// Auto-categorize freelance projects
export const getFreelanceProjectsByCategory = () => {
    const categories = {};

    freelanceProjects.forEach(project => {
        const category = project.category || 'Other';
        if (!categories[category]) {
            categories[category] = [];
        }
        categories[category].push(project);
    });

    return categories;
};

